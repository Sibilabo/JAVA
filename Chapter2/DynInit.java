
//Demonstracja dynamicznej inicjalizacji

class DynInit {
    public static void main(String args[]) {

        double radius = 4, height = 5;

        //Dynamiczna inicjalizacja zmiennej volume
        double volume = 3.1416 * radius * height;

        System.out.println("Poejmność walca wynosi " + volume);
    }
}
